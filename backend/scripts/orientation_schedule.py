# Summary statistics
def get_schedule_summary():
    """Get summary statistics about the orientation schedule"""
    total_events = len(ORIENTATION_EVENTS)
    total_leaders_needed = sum(event["leaders_needed"] for event in ORIENTATION_EVENTS.values())
    
    # Count different event types
    meal_events = sum(1 for event in ORIENTATION_EVENTS.values() if event["is_meal"])
    core_events = sum(1 for event in ORIENTATION_EVENTS.values() if event["is_core"])
    indoor_events = sum(1 for event in ORIENTATION_EVENTS.values() if event["is_indoor"])
    outdoor_events = sum(1 for event in ORIENTATION_EVENTS.values() if event["is_outdoor"])
    
    # Group by date
    events_by_date = {}
    for event_name, event_data in ORIENTATION_EVENTS.items():
        date = event_data["date"]
        if date not in events_by_date:
            events_by_date[date] = []
        events_by_date[date].append({
            "name": event_name,
            "leaders_needed": event_data["leaders_needed"],
            "start_time": event_data["start_time"],
            "end_time": event_data["end_time"],
            "is_meal": event_data["is_meal"],
            "is_core": event_data["is_core"],
            "is_indoor": event_data["is_indoor"],
            "is_outdoor": event_data["is_outdoor"]
        })
    
    return {
        "total_events": total_events,
        "total_leaders_needed": total_leaders_needed,
        "meal_events": meal_events,
        "core_events": core_events,
        "indoor_events": indoor_events,
        "outdoor_events": outdoor_events,
        "events_by_date": events_by_date
    }

def get_meal_events():
    """Get all meal events"""
    return {name: data for name, data in ORIENTATION_EVENTS.items() if data["is_meal"]}

def get_core_events():
    """Get all core events"""
    return {name: data for name, data in ORIENTATION_EVENTS.items() if data["is_core"]}

def get_outdoor_events():
    """Get all outdoor events"""
    return {name: data for name, data in ORIENTATION_EVENTS.items() if data["is_outdoor"]}

