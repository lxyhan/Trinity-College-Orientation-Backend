# Summary statistics
def get_schedule_summary():
    """Get summary statistics about the orientation schedule"""
    total_events = len(ORIENTATION_EVENTS)
    total_leaders_needed = sum(event["leaders_needed"] for event in ORIENTATION_EVENTS.values())
    
    # Group by date
    events_by_date = {}
    for event_name, event_data in ORIENTATION_EVENTS.items():
        date = event_data["date"]
        if date not in events_by_date:
            events_by_date[date] = []
        events_by_date[date].append({
            "name": event_name,
            "leaders_needed": event_data["leaders_needed"],
            "start_time": event_data["start_time"],
            "end_time": event_data["end_time"]
        })
    
    return {
        "total_events": total_events,
        "total_leaders_needed": total_leaders_needed,
        "events_by_date": events_by_date
    }

if __name__ == "__main__":
    # Print schedule summary
    summary = get_schedule_summary()
    print("Trinchantment 2025 Orientation Schedule Summary")
    print("=" * 50)
    print(f"Total Events: {summary['total_events']}")
    print(f"Total Leaders Needed: {summary['total_leaders_needed']}")
    print("\nEvents by Date:")
    
    for date in sorted(summary["events_by_date"].keys()):
        events = summary["events_by_date"][date]
        print(f"\n{date}:")
        for event in events:
            print(f"  {event['start_time']}-{event['end_time']}: {event['name']} ({event['leaders_needed']} leaders)")
